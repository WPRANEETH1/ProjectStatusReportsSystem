package com.ongoingsolution.model;

/**
 *
 * @author Praneeth
 */
public class Issues {
    private String issuesId;
    private String issuesProjectName;
    private String issuesDescription;
    private String issuesRank;
    private String issuesType;

    /**
     * @return the issuesId
     */
    public String getIssuesId() {
        return issuesId;
    }

    /**
     * @param issuesId the issuesId to set
     */
    public void setIssuesId(String issuesId) {
        this.issuesId = issuesId;
    }

    /**
     * @return the issuesProjectName
     */
    public String getIssuesProjectName() {
        return issuesProjectName;
    }

    /**
     * @param issuesProjectName the issuesProjectName to set
     */
    public void setIssuesProjectName(String issuesProjectName) {
        this.issuesProjectName = issuesProjectName;
    }

    /**
     * @return the issuesDescription
     */
    public String getIssuesDescription() {
        return issuesDescription;
    }

    /**
     * @param issuesDescription the issuesDescription to set
     */
    public void setIssuesDescription(String issuesDescription) {
        this.issuesDescription = issuesDescription;
    }

    /**
     * @return the issuesRank
     */
    public String getIssuesRank() {
        return issuesRank;
    }

    /**
     * @param issuesRank the issuesRank to set
     */
    public void setIssuesRank(String issuesRank) {
        this.issuesRank = issuesRank;
    }

    /**
     * @return the issuesType
     */
    public String getIssuesType() {
        return issuesType;
    }

    /**
     * @param issuesType the issuesType to set
     */
    public void setIssuesType(String issuesType) {
        this.issuesType = issuesType;
    }

   
    
}
